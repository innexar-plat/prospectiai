const FEATURE_KEYS_COUNT = 4;

export function useI18n(initialLocale: string = 'pt') {
    const [locale, setLocale] = useState(initialLocale);

    const t = useCallback((key: string, options?: Record<string, unknown>) => {
        let message = MESSAGES[locale]?.[key] || key;

        if (options) {
            Object.entries(options).forEach(([k, v]) => {
                message = message.replace(`{${k}}`, String(v));
            });
        }

        return message;
    }, [locale]);

    const raw = useCallback(
        (prefix: string): string[] => {
            const out: string[] = [];
            for (let i = 1; i <= FEATURE_KEYS_COUNT; i++) {
                const k = `${prefix}.${i}`;
                const v = MESSAGES[locale]?.[k] ?? k;
                if (v === k) break;
                out.push(v);
            }
            return out;
        },
        [locale]
    );

    return { t, raw, locale, setLocale };
}
